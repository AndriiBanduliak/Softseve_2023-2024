def max_num(num1, num2):
    """Returns the largest number of two numbers.

    Parameters:
    num1 (int or float): The first number to compare.
    num2 (int or float): The second number to compare.

    Returns:
    int or float: The larger number of num1 and num2.
    """
    if num1 > num2:
        return num1
    else:
        return num2
